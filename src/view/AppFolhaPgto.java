    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed
        if (jTFMatricula.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo matrícula", 
                                                                  "Aviso", 0);
            jTFMatricula.setText(null);
            jTFMatricula.requestFocusInWindow();
            return;
        }
        if (jTFNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo nome", 
                                                                  "Aviso", 0);
            jTFNome.setText(null);
            jTFNome.requestFocusInWindow();
            return;
        }
        if (jTFSobrenome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo sobrenome",
                    "Aviso", 0);
            jTFSobrenome.setText(null);
            jTFSobrenome.requestFocusInWindow();
            return;
        }

        String campoSemMasc = Utilit.removeMasc(jFTFCpf.getText());

        if (campoSemMasc.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Preencha o campo CPF", 
                                                                  "SisFolhaPgto 1.0", 0);
            jFTFCpf.requestFocusInWindow();
            return;
        }

        String tipoLogradouro = "", uf = "";
        if (jCBTipo.getSelectedIndex() != -1) {
            tipoLogradouro = jCBTipo.getSelectedItem().toString();
            if (jTFLogradouro.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo logradouro", 
                                                                      "SisFolhaPgto 1.0", 0);
                jTFLogradouro.requestFocusInWindow();
                return;
            }
        }

        if (jCBUf.getSelectedIndex() != -1) {
            uf = jCBUf.getSelectedItem().toString();
        }
        //fazer demais consistências

        Endereco endereco = new Endereco(tipoLogradouro + " " + 
                              jTFLogradouro.getText(), jTFNumero.getText(), jTFBairro.getText(), 
                              jTFCidade.getText(), uf, jFTFCep.getText().trim());

        if (jTPTipoEmpregado.getSelectedIndex() == 0) {
            if (jCBCargo.getSelectedIndex() != -1) {
                empregado = new EmpregadoMensalista(jCBCargo.getSelectedItem().toString(), 
                                   jTFMatricula.getText(), jTFNome.getText(), jTFSobrenome.getText(),
                                  jFTFCpf.getText(), endereco);
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um cargo para o empregado", 
                                                                     "SisFolhaPgto 1.0", 0);
                return;
            }
        } else if (jTPTipoEmpregado.getSelectedIndex() == 1) {
            int hora = (Integer) (jSTotalHorasTrab.getValue());
            if (hora == 0) {
                JOptionPane.showMessageDialog(this, "Preencha o campo 'Total de horas'", 
                                                                      "SisFolhaPgto 1.0", 0);
                jSTotalHorasTrab.requestFocusInWindow();
                return;
            }

            if (jTFValorHora.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo valor da hora", 
                                                                     "SisFolhaPgto 1.0", 0);
                jTFValorHora.requestFocusInWindow();
                return;
            }

            float valorHora = Float.parseFloat(jTFValorHora.getText());
            
            empregado = new EmpregadoHorista(jTFMatricula.getText(), 
                                  jTFNome.getText(), jTFSobrenome.getText(), jFTFCpf.getText(), 
                                  endereco, hora, valorHora);
        }//EmpregadoHorista

        DecimalFormat mascaraNum = new DecimalFormat("R$ #,##0.00");
        jTAResultado.setText(empregado.dados());
        jTFSalarioBase.setText(mascaraNum.format(empregado.getSalario()));
        jTFSalarioLiquido.setText(mascaraNum.format(empregado.calculaSalarioLiquido()));
        jTFInss.setText(mascaraNum.format(empregado.calculaInss()));
        jTFIrpf.setText(mascaraNum.format(empregado.calculaIR()));

    }//GEN-LAST:event_jBCalcularActionPerformed

