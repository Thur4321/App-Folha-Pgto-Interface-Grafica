    private void limpar() {
        limpaCampos(jPDadosPessoais);
        limpaCampos(jPEndereco);
        jTAResultado.setText(null);
        limpaCampos(jPSalario);
        limpaCampos(jPDescontos);
        limpaCampos(jPEmpregadoHorista);
        limpaCampos(jPEmpregadoMensalista);
        jTFMatricula.requestFocusInWindow();
    }

    private void limpaCampos(javax.swing.JPanel painel) {
        java.awt.Component[] componentes = painel.getComponents();
        for (int i = 0; i < componentes.length; i++) {
            if (componentes[i] instanceof javax.swing.JTextField) {
                javax.swing.JTextField campo = (javax.swing.JTextField) componentes[i];
                campo.setText(null);
            }

            if (componentes[i] instanceof javax.swing.JComboBox) {
                javax.swing.JComboBox combo = (javax.swing.JComboBox) componentes[i];
                combo.setSelectedIndex(-1);
            }

            if (componentes[i] instanceof javax.swing.JSpinner) {
                javax.swing.JSpinner campoSpinner = (javax.swing.JSpinner) componentes[i];
                campoSpinner.setValue(0);
            }
        }
    }

